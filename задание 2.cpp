#include <iostream>   // подключаем библиотеку для ввода/вывода
using namespace std;  // чтобы не писать std:: перед cout

int main() {
    int i, j;  // объявляем переменные для циклов

    // внешний цикл — отвечает за строки (с 4 до 0)
    for (i = 4; i >= 0; i--) {

        // внутренний цикл — выводит числа от 0 до i
        for (j = 0; j <= i; j++) {
            cout << j << " ";  // выводим число и пробел
        }

        cout << endl;  // переход на новую строку после каждой строки
    }

    return 0;  // конец программы
}